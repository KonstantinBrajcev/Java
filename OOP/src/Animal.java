// Базовый класс Animal
public class Animal {
    // Методы двигаться, летать и плавать
    public void toGo() {
        System.out.println("Это животное может передвигаться.");
    }

    public void fly() {
        System.out.println("Это животное может летать.");
    }

    public void swim() {
        System.out.println("Это животное может плавать.");
    }
}